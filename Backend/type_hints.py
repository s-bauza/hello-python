# Variables

my_string_varaible = "My String variable"
print(my_string_varaible)
print(type(my_string_varaible))#<class 'str'>

my_string_varaible = 5
print(my_string_varaible)
print(type(my_string_varaible))#<class 'int'>

my_typed_variable: str = "My Typed String variable"
print(my_typed_variable)
print(type(my_typed_variable))#<class 'str'>

my_typed_variable: int = 5
print(my_typed_variable)
print(type(my_typed_variable))#<class 'int'>