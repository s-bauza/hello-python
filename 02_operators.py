#Arithmetic operators

#operations with integers
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 2)
print(10 // 3) # floor division
print(2 ** 3) 
print(2**3+3-7/1//4)
print(8+3-7//4)

#Operations with text strings
print("Hello"+"world")
print("Hello"+str(5)) #converts the integer to a string

#Mix operations
print("Hello"*3)
print("Hello"*(2**3))

my_float = 3.14 * 2
print("Hello"*int(my_float)) #converts the float to an integer

#Comparison operators   

#Operations with integers
print(3 == 4)
print(3 != 4)
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)

#Operations with text strings
print("Hello" == "Hello")
print("Hello" != "Hello")
print("Hello" > "Hello")
print("Hello" < "Hello")
print("Hello" >= "Hello")
print("Hello" <= "Hello")
print(len("Hello") == len("Hello"))#compares the length of the strings

#Logical operators

